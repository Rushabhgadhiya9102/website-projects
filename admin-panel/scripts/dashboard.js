let sideBar = true;

function sidebarCustomization() {
  sideBar = !sideBar;
  if (sideBar) {
    $("#side-bar").css("width", "5%");
    $(".btnNameHide").css("display", "none");
  } else {
    $("#side-bar").css("width", "20%");
    $(".btnNameHide").css("display", "block");
  }
}

$(".bx-menu").on("click", function () {
  sidebarCustomization();
});

